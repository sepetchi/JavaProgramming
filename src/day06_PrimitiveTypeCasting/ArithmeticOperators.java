package day06_PrimitiveTypeCasting;

public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println("12"+ 1); //concatination
        System.out.println(10 + 20); // addition 30
        System.out.println(100 - 50); // subtraction 50
        System.out.println(10 * 5); // multiplication 50

        System.out.println(100 / 3);
        System.out.println(10.0 / 4);
        System.out.println(10 / 4.0);
        System.out.println(10.0/4.0);

        int a = 100;
        double b = a/6;
        System.out.println(b);

        double c = a/6.0;
        System.out.println(c);
        System.out.println(100D);

    }

    /*
    + addition
    - subtraction
    * multiplication
    / division
    % remainder

     */
}
